    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String passlama = JOptionPane.showInputDialog(null, "Masukan Password Lama Anda: ", "");
        PengelolaUser a =null;
        if(passlama.equals(this.Pass)){
            try {
                a = new PengelolaUser();
            } catch (IOException ex) {
                Logger.getLogger(EditUserUI.class.getName()).log(Level.SEVERE, null, ex);
            }
            a.ganti(this.Nama, jTextField_namabaru.getText(), this.Username, jTextField_usernamebaru.getText(), 
                    this.Pass, jPasswordField_passbaru.getText());
            JOptionPane.showMessageDialog(null, "Selesai", "", JOptionPane.INFORMATION_MESSAGE);
            MenuUserUI ui = new MenuUserUI(a.getNama(), a.getUsrnm(), a.getPass());
            ui.setVisible(true);
            this.setVisible(false);
        }else{
            JOptionPane.showMessageDialog(null, "Password tidak cocok!!", "", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

